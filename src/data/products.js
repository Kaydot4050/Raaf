export function formatPrice(min, max) {
  const currency = new Intl.NumberFormat('en-GH', { style: 'currency', currency: 'GHS' });
  const amount = new Intl.NumberFormat('en-GH', {
    minimumFractionDigits: 2,
    maximumFractionDigits: 2,
  });
  if (min === max) return currency.format(min);
  return `${currency.format(min)}\u00a0–\u00a0${amount.format(max)}`;
}

export function getProduct(id) {
  return products.find((p) => p.id === id);
}

export function getRelatedProducts(product, limit = 4) {
  return products
    .filter((p) => p.id !== product.id && p.category === product.category)
    .slice(0, limit);
}
